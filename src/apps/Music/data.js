/**
 * Group songs by artist
 * @param {Array} songs - Array of song objects
 * @returns {Object} Object with artistId as keys and arrays of songs as values
 */
export function groupSongsByArtist(songs) {
  if (!songs || !Array.isArray(songs)) return {};
  
  return songs.reduce((groups, song) => {
    const artistId = song.artistId;
    if (!groups[artistId]) {
      groups[artistId] = {
        artistId: artistId,
        artistName: song.artist,
        songs: []
      };
    }
    groups[artistId].songs.push(song);
    return groups;
  }, {});
}

/**
 * Group songs by album
 * @param {Array} songs - Array of song objects
 * @returns {Object} Object with albumId as keys and arrays of songs as values
 */
export function groupSongsByAlbum(songs) {
  if (!songs || !Array.isArray(songs)) return {};
  
  return songs.reduce((groups, song) => {
    const albumId = song.albumId;
    if (!groups[albumId]) {
      groups[albumId] = {
        albumId: albumId,
        albumName: song.album,
        artistName: song.artist,
        cover: song.cover,
        songs: []
      };
    }
    groups[albumId].songs.push(song);
    // Sort songs by track number within each album
    groups[albumId].songs.sort((a, b) => (a.trackNumber || 0) - (b.trackNumber || 0));
    return groups;
  }, {});
}

/**
 * Group songs by genre
 * @param {Array} songs - Array of song objects
 * @returns {Object} Object with genre as keys and arrays of songs as values
 */
export function groupSongsByGenre(songs) {
  if (!songs || !Array.isArray(songs)) return {};
  
  return songs.reduce((groups, song) => {
    const genre = song.genre || 'Unknown';
    if (!groups[genre]) {
      groups[genre] = {
        genre: genre,
        songs: []
      };
    }
    groups[genre].songs.push(song);
    return groups;
  }, {});
}

/**
 * Filter collection by search query (case-insensitive)
 * Searches across songs, artists, and albums
 * @param {Array} songs - Array of song objects
 * @param {Array} artists - Array of artist objects
 * @param {Array} albums - Array of album objects
 * @param {string} query - Search query string
 * @returns {Object} Filtered results with songs, artists, and albums arrays
 */
export function filterCollection(songs, artists, albums, query) {
  if (!query || typeof query !== 'string' || query.trim() === '') {
    return {
      songs: songs || [],
      artists: artists || [],
      albums: albums || []
    };
  }
  
  const lowerQuery = query.toLowerCase().trim();
  
  const filteredSongs = (songs || []).filter(song => 
    song.title.toLowerCase().includes(lowerQuery) ||
    song.artist.toLowerCase().includes(lowerQuery) ||
    song.album.toLowerCase().includes(lowerQuery)
  );
  
  const filteredArtists = (artists || []).filter(artist =>
    artist.name.toLowerCase().includes(lowerQuery)
  );
  
  const filteredAlbums = (albums || []).filter(album =>
    album.title.toLowerCase().includes(lowerQuery) ||
    album.artist.toLowerCase().includes(lowerQuery)
  );
  
  return {
    songs: filteredSongs,
    artists: filteredArtists,
    albums: filteredAlbums
  };
}

/**
 * Get all unique genres from songs
 * @param {Array} songs - Array of song objects
 * @returns {Array} Array of unique genre strings
 */
export function getUniqueGenres(songs) {
  if (!songs || !Array.isArray(songs)) return [];
  
  const genres = new Set();
  songs.forEach(song => {
    if (song.genre) {
      genres.add(song.genre);
    }
  });
  return Array.from(genres).sort();
}

/**
 * Get songs by genre
 * @param {Array} songs - Array of song objects
 * @param {string} genre - Genre to filter by
 * @returns {Array} Array of songs matching the genre
 */
export function getSongsByGenre(songs, genre) {
  if (!songs || !Array.isArray(songs) || !genre) return [];
  return songs.filter(song => song.genre === genre);
}

// Get ringtones grouped by category
export function getRingtonesByCategory() {
    const grouped = {
        nokia: [],
        windows: [],
        custom: []
    };
    
    RINGTONES.forEach(ringtone => {
        if (grouped[ringtone.category]) {
            grouped[ringtone.category].push(ringtone);
        }
    });
    
    return grouped;
}

// Format duration in seconds to display string
export function formatDuration(seconds) {
    if (seconds < 60) {
        return `${seconds}s`;
    }
    const minutes = Math.floor(seconds / 60);
    const remainingSeconds = seconds % 60;
    return remainingSeconds > 0 ? `${minutes}m ${remainingSeconds}s` : `${minutes}m`;
}

// Find ringtone by id
export function findRingtoneById(id) {
    return RINGTONES.find(r => r.id === id);
}

// Find ringtone by name
export function findRingtoneByName(name) {
    return RINGTONES.find(r => r.name === name);
}
